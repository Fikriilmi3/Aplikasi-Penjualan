    public void totalBiaya(){
        int jumlahBaris = tabel.getRowCount();
        int totalBiaya = 0;
        int jumlahBarang, hargaBarang;
        for (int i = 0; i < jumlahBaris; i++){
            jumlahBarang = Integer.parseInt(tabel.getValueAt(i, 3).toString());
            hargaBarang = Integer.parseInt(tabel.getValueAt(i, 4).toString());
            totalBiaya = totalBiaya + (jumlahBarang * hargaBarang);
        }
        totalbayar.setText(String.valueOf(totalBiaya));
        tampil.setText("Rp" + totalBiaya + ",00");
    }
    
    private void autonumber() {
        try {
            Connection c = (Connection) Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM transaksi ORDER BY NoFaktur DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String NoFaktur = r.getString("NoFaktur").substring(2);
                String TR = "" + (Integer.parseInt(NoFaktur)+1);
                String Nol = "";
                
                if (TR.length() == 1) {
                    Nol = "000";
                } else if (TR.length() == 2) {
                    Nol = "00";
                } else if (TR.length() == 3) {
                    Nol = "0";
                } else if (TR.length() == 4) {
                    Nol = "";
                }
                notransaksi.setText("TR" + Nol + TR);
            }else{
                notransaksi.setText("TR0001");
            }
            r.close();
            s.close();
        }catch (Exception e){
            System.out.println("autonumber error");
        }
    } 
    
    
    public void loadData(){
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        model.addRow(new Object[]{
            notransaksi.getText(),
            kdbarang.getText(),
            namabarang.getText(),
            jml.getText(),
            hargaa.getText(),
            totalbayar.getText()                
        });
    }
    
    public void kosong(){
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        
        while (model.getRowCount()>0){
            model.removeRow(0);
        }
    }
    
