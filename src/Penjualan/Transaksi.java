    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
        // TODO add your handling code here:
        int total, bayarr, kembalii;
        
        total = Integer.valueOf(totalbayar.getText());
        bayarr = Integer.valueOf(bayar.getText());
        
        if(total > bayarr){
            JOptionPane.showMessageDialog(null, "Uang tidak cukup untuk melakukan pembayaran");
        }else{
            kembalii = bayarr - total;
            kembali.setText(String.valueOf(kembalii));
        }
    }//GEN-LAST:event_bayarActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        
        String noTransaksi = notransaksi.getText();
        String tanggal = tgl.getText();
        String kasirr = kasir.getText();
        String total = totalbayar.getText();
        
        
        try{
            Connection c = (Connection) Koneksi.getKoneksi();
            String sql = "INSERT INTO transaksi VALUES (?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, noTransaksi);
            p.setString(2, tanggal);
            p.setString(3, kasirr);
            p.setString(4, total);
            p.executeUpdate();
            p.close();
        }catch (Exception e){
           System.out.println("Simpan penjualan error");
        }
        try{
           Connection c = (Connection) Koneksi.getKoneksi();
          int baris = tabel.getRowCount();
           
           for (int i = 0; i < baris; i++){
               String sql = "INSERT INTO rinci (NoFaktur, Kd_Barang, Nama_Barang, Jumlah, Harga, Total) VALUES ('"
                       + tabel.getValueAt(i, 0) +"', '"+ tabel.getValueAt(i, 1) +"','"+ tabel.getValueAt(i, 2) 
                       +"','"+ tabel.getValueAt(i, 3) +"','"+ tabel.getValueAt(i, 4) +"','"+ tabel.getValueAt(i, 5) 
                       +"')";
               PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
           }
        }catch (Exception e){
            System.out.println("simpan transaksi penjualan error");
        }
        clear();
        utama();
        autonumber();
        kosong();
        tampil.setText("Rp. 0");
        
        
    }//GEN-LAST:event_simpanActionPerformed

